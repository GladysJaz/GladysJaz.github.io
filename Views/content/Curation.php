                <h1>¿Qué son las curadurías?</h1>
				 <h1>Comunicacion y Salud</h1>
                <p <i class="fa fa-align-center" aria-hidden="true"></i>
                    Un curador, es quien cura algo, definición más que apropiada para definir la labor de la curaduría.
                    En un mundo que se caracteriza por tener todo, o la mayoría de las cosas, al alcance de la mano; 
                    la información, una compra, un texto, acceso a lugares sin estar presente físicamente; nos presenta un problema y es que, 
                    tenemos demasiado, pero, queremos siempre más. Parece no bastar.
                    La curaduría viene a presentar una solución a este fenómeno, más allá de las galerías o los museos. 
                    Se trata de tomar todo este vasto cofre de “demasiados” y realizar un proceso de selección y refinamiento; 
                    un proceso que nos dará como resultado evitar la saturación. Esta actividad no está limitada a la información en la web, 
                    es algo que ya se está implementando en el campo de la economía, el empresarial, pequeños y grandes negocios, y las personas en su vida privada lo están haciendo. 
                    Se apuesta por quedarse con lo realmente útil; calidad antes que cantidad.
                    La curaduría tiene la función de un filtro, ante este constante fluido de cosas. Es simplificar y contextualizar.
                    La curaduría nos ayuda a ahorrar tiempo, a maximizar la utilidad, y sobre todo, nos ayuda a superar esta saturación de información, 
                    entre muchos otros beneficios.
                    (Ref. Bhaskar, Michael. Curaduría, El poder de la selección en un mundo de excesos).
                </p>
